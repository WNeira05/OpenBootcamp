package ejerciciosTema1;

public class TipoDeDatos {

    public static void main(String[] args) {

        int numberInt = 2567;
        long numberLong = -92124;
        double numberDouble = 41246367;
        float numberFloat = 3.14f;
        boolean verdadero = true;
        char letra = 'w';
        String HelloWorld = "Hola Mundo";


        System.out.println("Numero entero: "+ numberInt);
        System.out.println("Numero long: "+ numberLong);
        System.out.println("Numero Double: "+ numberDouble);
        System.out.println("Numero float: "+ numberFloat);
        System.out.println("Boolean: "+ verdadero);
        System.out.println("Letra: "+ letra);
        System.out.println("Cadena de caracteres: "+ HelloWorld);


    }


}
